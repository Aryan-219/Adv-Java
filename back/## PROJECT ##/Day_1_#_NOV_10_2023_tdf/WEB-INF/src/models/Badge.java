package models;

public class Badge {
    private Integer badgeId;
    private String name;
    public Badge() {
    }
    public Integer getBadgeId() {
        return badgeId;
    }
    public void setBadgeId(Integer badgeId) {
        this.badgeId = badgeId;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
}
